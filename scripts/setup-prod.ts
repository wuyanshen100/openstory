function loadExistingEnv(): Map<string, string> {
  const env = parseEnvFile(ENV_FILE);

  // Also read legacy .env so existing keys are migrated
  const legacy = parseEnvFile(resolve(process.cwd(), '.env'));
  legacy.forEach((value, key) => {
    if (!env.has(key)) env.set(key, value);
  });

  return env;
}

function writeEnvFile(vars: Map<string, string>) {
  const lines: string[] = [
    '# =============================================================================',
    '# Production Configuration',
    '# Generated by `bun setup --prod` — safe to edit',
    '# =============================================================================',
    '',
  ];

  const sections: Array<{ header: string; keys: string[] }> = [
    {
      header: 'Core',
      keys: [
        'VITE_APP_URL',
        'VITE_APP_NAME',
        'BETTER_AUTH_SECRET',
        'CLOUDFLARE_ACCOUNT_ID',
        'CLOUDFLARE_API_TOKEN',
        'CLOUDFLARE_ZONE_ID',
      ],
    },
    {
      header: 'Email (Cloudflare Email Service)',
      keys: ['EMAIL_FROM'],
    },
    {
      header: 'AI Services',
      keys: ['FAL_KEY', 'OPENROUTER_KEY'],
    },
    {
      header: 'Storage (Cloudflare R2)',
      keys: [
        'R2_ACCOUNT_ID',
        'R2_ACCESS_KEY_ID',
        'R2_SECRET_ACCESS_KEY',
        'R2_BUCKET_NAME',
        'R2_PUBLIC_STORAGE_DOMAIN',
        'R2_PUBLIC_ASSETS_BUCKET',
        'VITE_R2_PUBLIC_ASSETS_DOMAIN',
      ],
    },
    {
      header: 'Google OAuth',
      keys: ['GOOGLE_CLIENT_ID', 'GOOGLE_CLIENT_SECRET'],
    },
    {
      header: 'Observability (Langfuse)',
      keys: [
        'LANGFUSE_PUBLIC_KEY',
        'LANGFUSE_SECRET_KEY',
        'LANGFUSE_BASE_URL',
        'LANGFUSE_PROMPTS_ENABLED',
      ],
    },
    {
      header: 'Analytics (PostHog)',
      keys: ['VITE_PUBLIC_POSTHOG_PROJECT_TOKEN', 'VITE_PUBLIC_POSTHOG_HOST'],
    },
    {
      header: 'Billing (Stripe)',
      keys: ['STRIPE_SECRET_KEY', 'STRIPE_WEBHOOK_SECRET'],
    },
    {
      header: 'Security',
      keys: ['API_KEY_ENCRYPTION_KEY'],
    },
  ];

  for (const section of sections) {
    const sectionVars = section.keys.filter((k) => vars.has(k));
    if (sectionVars.length === 0) continue;

    lines.push(`# ${section.header}`);
    for (const key of sectionVars) {
      lines.push(`${key}=${vars.get(key)}`);
    }
    lines.push('');
  }

  writeFileSync(ENV_FILE, lines.join('\n'));
}

function commandExists(cmd: string): boolean {
  try {
    execFileSync('which', [cmd], { stdio: 'ignore' });
    return true;
  } catch {
    return false;
  }
}

