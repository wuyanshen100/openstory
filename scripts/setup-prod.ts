export async function runProdSetup(mode: ProdSetupMode) {
  const existing = loadExistingEnv();
  const vars = new Map(existing);

  // Save progress incrementally so users can cancel mid-setup
  const saveProgress = () => writeEnvFile(vars);

  process.on('SIGINT', () => {
    saveProgress();
    console.log('');
    p.cancel(`Setup cancelled. Progress saved to ${ENV_FILENAME}`);
    process.exit(0);
  });

  function checkCancel<T>(value: T | symbol): T {
    if (p.isCancel(value)) {
      saveProgress();
      p.cancel(`Setup cancelled. Progress saved to ${ENV_FILENAME}`);
      process.exit(0);
    }
    return value;
  }

  // --pr-preview: push secrets to GitHub staging environment for PR previews
  if (mode === 'pr-preview') {
    await prPreviewSetup();
    return;
  }

  // --deploy: skip env var prompts, jump straight to deploy phase
  if (mode === 'deploy') {
    p.intro(chalk.bold('OpenStory Deploy Setup'));

    if (existing.size === 0) {
      p.log.error(
        `No ${ENV_FILENAME} found. Run ${chalk.bold('bun setup --prod')} first to configure environment variables.`
      );
      process.exit(1);
    }

    p.log.info(`Loaded ${existing.size} values from ${ENV_FILENAME}`);

    await deploySetup(vars, checkCancel);
    p.outro('Deploy setup complete.');
    return;
  }

  p.intro(chalk.bold('OpenStory Production Setup'));

  async function promptForKey(
    key: string,
    message: string,
    hint?: string
  ): Promise<void> {
    if (vars.has(key)) {
      p.log.success(`${key} — already configured`);
      return;
    }

    const raw = checkCancel(
      await p.text({
        message: hint ? `${message}\n${chalk.dim(hint)}` : message,
        placeholder: `Paste your ${key} here…`,
      })
    );

    if (!raw || !raw.trim()) return;

    // Try parsing as KEY=VALUE (stores under the parsed key, not the expected key,
    // so pasting KEY=VALUE into any prompt works correctly)
    const parsed = parseEnvString(raw);
    const envPairs = new Map<string, string>();
    for (const [k, v] of parsed) {
      if (/^[A-Z_][A-Z0-9_]*$/.test(k)) {
        envPairs.set(k, v);
      }
    }

    if (envPairs.size > 0) {
      for (const [k, v] of envPairs) {
        vars.set(k, v);
      }
      saveProgress();
      return;
    }

    // Raw value without KEY= prefix — store under expected key, strip quotes
    let value = raw.trim();
    if (
      value.length >= 2 &&
      ((value[0] === '"' && value.at(-1) === '"') ||
        (value[0] === "'" && value.at(-1) === "'"))
    ) {
      value = value.slice(1, -1);
    }

    vars.set(key, value);
    saveProgress();
  }

  async function attachR2Domains(): Promise<void> {
    const storageDomain = vars.get('R2_PUBLIC_STORAGE_DOMAIN');
    const assetsDomain = vars.get('VITE_R2_PUBLIC_ASSETS_DOMAIN');

    if (!storageDomain && !assetsDomain) return;

    const attachDomains = checkCancel(
      await p.confirm({
        message: 'Attach custom domains to R2 buckets via Cloudflare?',
        initialValue: true,
      })
    );

    if (attachDomains) {
      if (!vars.has('CLOUDFLARE_ZONE_ID')) {
        // Try to auto-detect zone ID from the domain name
        const domain = storageDomain ?? assetsDomain;
        const baseDomain = domain?.split('.').slice(-2).join('.');
        const apiToken = vars.get('CLOUDFLARE_API_TOKEN');

        if (apiToken && baseDomain) {
          const zoneSpinner = p.spinner();
          zoneSpinner.start(`Looking up zone ID for ${baseDomain}`);
          try {
            const res = await fetch(
              `https://api.cloudflare.com/client/v4/zones?name=${baseDomain}`,
              { headers: { Authorization: `Bearer ${apiToken}` } }
            );
            const data: { success?: boolean; result?: { id: string }[] } =
              await res.json();
            const firstZone = data.success ? data.result?.[0] : undefined;
            if (firstZone) {
              const zoneId = firstZone.id;
              vars.set('CLOUDFLARE_ZONE_ID', zoneId);
              saveProgress();
              zoneSpinner.stop(
                `Found zone ID for ${baseDomain}: ${zoneId.slice(0, 8)}…`
              );
            } else {
              zoneSpinner.stop(`Could not find zone for ${baseDomain}`);
            }
          } catch {
            zoneSpinner.stop('Zone lookup failed');
          }
        }

        if (!vars.has('CLOUDFLARE_ZONE_ID')) {
          await promptForKey(
            'CLOUDFLARE_ZONE_ID',
            'Cloudflare Zone ID',
            'Find in Cloudflare Dashboard > your domain > Overview'
          );
        }
      }
      const zoneId = vars.get('CLOUDFLARE_ZONE_ID');
      if (!zoneId) {
        p.log.warn('No zone ID provided — skipping domain attachment.');
        return;
      }

      const storageBucket = vars.get('R2_BUCKET_NAME') ?? 'openstory-storage';
      const assetsBucket =
        vars.get('R2_PUBLIC_ASSETS_BUCKET') ?? 'openstory-public-assets';

      for (const [domain, bucket] of [
        [storageDomain, storageBucket],
        [assetsDomain, assetsBucket],
      ] as const) {
        if (!domain) continue;
        if (bucketHasDomain(bucket, domain)) {
          p.log.success(`${domain} → ${bucket} (already attached)`);
          continue;
        }
        try {
          execFileSync(
            'bunx',
            [
              'wrangler',
              'r2',
              'bucket',
              'domain',
              'add',
              bucket,
              '--domain',
              domain,
              '--zone-id',
              zoneId,
              '--force',
            ],
            { stdio: 'inherit' }
          );
          p.log.success(`Attached ${domain} → ${bucket}`);
        } catch {
          p.log.warn(`Could not attach ${domain} to ${bucket}`);
        }
      }
    }
  }

  if (existing.size > 0) {
    p.log.info(
      `Found existing ${ENV_FILENAME} with ${existing.size} configured values`
    );
  }

  // -------------------------------------------------------------------------
  // Email (Cloudflare Email Service)
  // -------------------------------------------------------------------------
  p.log.step(chalk.bold('Email Setup'));
  p.log.info(
    'Email is sent via Cloudflare Email Service (the SEND_EMAIL binding).\n' +
      'The sender domain must be onboarded under Email > Email Sending in\n' +
      'the Cloudflare dashboard before production emails will deliver.'
  );
  if (!vars.has('EMAIL_FROM')) {
    const emailFrom = checkCancel(
      await p.text({
        message: 'Enter sender email address',
        placeholder: 'noreply@example.com',
        validate: (v) =>
          !v ? 'Sender email is required for production' : undefined,
      })
    );
    if (emailFrom) {
      vars.set('EMAIL_FROM', emailFrom);
      saveProgress();
    }
  } else {
    p.log.success('EMAIL_FROM — already configured');
  }

  // -------------------------------------------------------------------------
  // Core
  // -------------------------------------------------------------------------
  p.log.step(chalk.bold('Core Setup'));

  await promptForKey(
    'VITE_APP_URL',
    'Enter your production URL',
    'e.g. https://app.example.com'
  );

  p.log.step(chalk.bold('Cloudflare'));
  await promptForKey(
    'CLOUDFLARE_ACCOUNT_ID',
    'Enter your Cloudflare Account ID',
    'Find in Cloudflare console → Compute → Workers & Pages → Account ID (sidebar)'
  );
  await promptForKey(
    'CLOUDFLARE_API_TOKEN',
    'Enter your Cloudflare API token',
    'Create at: https://dash.cloudflare.com/profile/api-tokens (needs D1 + Workers edit permissions)'
  );
  p.log.info(
    'D1 database is configured in wrangler.jsonc — deploy handles migrations automatically.'
  );

  if (!vars.has('VITE_APP_NAME')) vars.set('VITE_APP_NAME', 'OpenStory');

  // Contact/privacy emails are derived from VITE_APP_URL at runtime
  // (src/lib/marketing/constants.ts) — no env vars needed.

  if (!vars.has('BETTER_AUTH_SECRET')) {
    vars.set('BETTER_AUTH_SECRET', generateSecret());
    p.log.success('Auth secret generated');
  } else {
    p.log.success('Auth secret — already configured');
  }

  if (!vars.has('API_KEY_ENCRYPTION_KEY')) {
    vars.set('API_KEY_ENCRYPTION_KEY', generateSecret());
    p.log.success('Encryption key generated');
  } else {
    p.log.success('Encryption key — already configured');
  }

  p.log.success('Database: Cloudflare D1 (via wrangler.jsonc)');
  p.log.success(`App URL: ${vars.get('VITE_APP_URL')}`);
  saveProgress();

  // -------------------------------------------------------------------------
  // AI Generation (FAL_KEY)
  // -------------------------------------------------------------------------
  if (vars.has('FAL_KEY')) {
    p.log.success('FAL_KEY — already configured');
  } else {
    const setupAI = checkCancel(
      await p.confirm({
        message: 'Set up AI image & video generation?',
        initialValue: true,
      })
    );

    if (setupAI) {
      await promptForKey(
        'FAL_KEY',
        'Enter your Fal.ai API key',
        'Get one at: https://fal.ai/dashboard/keys'
      );
    }
  }

  // -------------------------------------------------------------------------
  // Script Analysis (OPENROUTER_KEY)
  // -------------------------------------------------------------------------
  if (vars.has('OPENROUTER_KEY')) {
    p.log.success('OPENROUTER_KEY — already configured');
  } else {
    const setupLLM = checkCancel(
      await p.confirm({
        message: 'Set up LLM-powered script analysis?',
        initialValue: true,
      })
    );

    if (setupLLM) {
      await promptForKey(
        'OPENROUTER_KEY',
        'Enter your OpenRouter API key',
        'Get one at: https://openrouter.ai/settings/keys'
      );
    }
  }

  // Async workflows run on Cloudflare Workflows (bindings in wrangler.jsonc).
  // Realtime progress updates run on the `REALTIME` Durable Object. Neither
  // needs any external service configuration.

  // -------------------------------------------------------------------------
  // Storage (R2)
  // -------------------------------------------------------------------------
  p.log.info(
    'R2 storage is handled by native bindings configured in wrangler.jsonc.\n' +
      'S3 credentials (for signed URLs) can be added to .env and deployed\n' +
      'via `bun setup-cloudflare-secrets.sh`.'
  );

  if (
    !vars.has('R2_PUBLIC_STORAGE_DOMAIN') ||
    !vars.has('VITE_R2_PUBLIC_ASSETS_DOMAIN')
  ) {
    // Derive R2 domains from VITE_APP_URL base domain
    const appUrl = vars.get('VITE_APP_URL') ?? '';
    let baseDomain = '';
    try {
      const host = new URL(appUrl).hostname; // e.g. openstory.so
      const parts = host.split('.');
      baseDomain = parts.length >= 2 ? parts.slice(-2).join('.') : host;
    } catch {
      // VITE_APP_URL not set or invalid — fall through to manual prompt
    }

    const isStaging = checkCancel(
      await p.select({
        message: 'Which R2 environment is this?',
        options: [
          { value: 'production', label: 'Production' },
          {
            value: 'staging',
            label: 'Staging / Preview',
            hint: 'uses -stg/-dev subdomains',
          },
        ],
      })
    );

    const storageSub = isStaging === 'staging' ? 'storage-stg' : 'storage';
    const assetsSub = 'assets';

    if (baseDomain) {
      const storageDomain = `${storageSub}.${baseDomain}`;
      const assetsDomain = `${assetsSub}.${baseDomain}`;

      p.log.info(
        `Derived from ${chalk.bold(baseDomain)}:\n` +
          `  R2_PUBLIC_STORAGE_DOMAIN = ${storageDomain}\n` +
          `  VITE_R2_PUBLIC_ASSETS_DOMAIN  = ${assetsDomain}`
      );

      const useDefaults = checkCancel(
        await p.confirm({
          message: 'Use these derived domains?',
          initialValue: true,
        })
      );

      if (useDefaults) {
        vars.set('R2_PUBLIC_STORAGE_DOMAIN', storageDomain);
        vars.set('VITE_R2_PUBLIC_ASSETS_DOMAIN', assetsDomain);
        saveProgress();
      } else {
        await promptForKey(
          'R2_PUBLIC_STORAGE_DOMAIN',
          `R2 Public Storage Domain (e.g. ${storageSub}.example.com)`
        );
        await promptForKey(
          'VITE_R2_PUBLIC_ASSETS_DOMAIN',
          `R2 Public Assets Domain (e.g. ${assetsSub}.example.com)`
        );
      }
    } else {
      await promptForKey(
        'R2_PUBLIC_STORAGE_DOMAIN',
        `R2 Public Storage Domain (e.g. ${storageSub}.example.com)`
      );
      await promptForKey(
        'VITE_R2_PUBLIC_ASSETS_DOMAIN',
        `R2 Public Assets Domain (e.g. ${assetsSub}.example.com)`
      );
    }
  } else {
    p.log.success(
      `R2_PUBLIC_STORAGE_DOMAIN = ${vars.get('R2_PUBLIC_STORAGE_DOMAIN')}`
    );
    p.log.success(
      `VITE_R2_PUBLIC_ASSETS_DOMAIN  = ${vars.get('VITE_R2_PUBLIC_ASSETS_DOMAIN')}`
    );
  }

  // Bucket names — needed for CORS configuration. Cloudflare runtime uses
  // native bindings from wrangler.jsonc, but wrangler CLI commands like
  // `r2 bucket cors set` need the bucket name explicitly.
  if (!vars.has('R2_BUCKET_NAME')) {
    vars.set('R2_BUCKET_NAME', 'openstory-storage');
    saveProgress();
  }
  if (!vars.has('R2_PUBLIC_ASSETS_BUCKET')) {
    vars.set('R2_PUBLIC_ASSETS_BUCKET', 'openstory-public-assets');
    saveProgress();
  }

  // Configure CORS on R2 bucket. Needed for:
  //   - Presigned PUT uploads from the browser (S3-style deployments).
  //   - Browser-side fetches from JS (e.g. browser-merge in #638) on every
  //     deployment, including Cloudflare. Native HTML elements (<video>,
  //     <img>, <a>) load R2 cross-origin without CORS, but `fetch()` does not.
  // Wrangler authenticates via CLOUDFLARE_API_TOKEN (or global wrangler login);
  // R2_ACCESS_KEY_ID is for runtime signed URLs, not for setting CORS.
  if (
    vars.has('R2_BUCKET_NAME') &&
    (vars.has('CLOUDFLARE_API_TOKEN') || vars.has('R2_ACCESS_KEY_ID'))
  ) {
    const setupCors = checkCancel(
      await p.confirm({
        message:
          'Configure CORS on R2 bucket? (required for direct uploads from browser)',
        initialValue: true,
      })
    );

    if (setupCors) {
      const bucketName = vars.get('R2_BUCKET_NAME');
      if (!bucketName) {
        p.log.error('R2_BUCKET_NAME is not set');
        return;
      }
      const appUrl = vars.get('VITE_APP_URL') ?? 'http://localhost:3000';
      const origins = new Set(['http://localhost:3000']);
      if (appUrl !== 'http://localhost:3000') origins.add(appUrl);

      const rules = buildRules({
        origins: [...origins],
        includeWrites: true,
      });
      const corsSpinner = p.spinner();
      corsSpinner.start(`Configuring CORS on ${bucketName}`);
      try {
        setBucketCors(bucketName, rules);
        corsSpinner.stop(`CORS configured on ${bucketName}`);
      } catch {
        corsSpinner.stop('CORS configuration failed');
        p.log.warn(
          'Configure manually in Cloudflare Dashboard:\n' +
            `  R2 → ${bucketName} → Settings → CORS Policy\n\n` +
            JSON.stringify({ rules }, null, 2)
        );
      }
    }
  }

  // Offer to attach custom domains to R2 buckets
  await attachR2Domains();

  // Cloudflare Image Resizing reminder — needed for all environments since
  // cdn-cgi/image/ URLs are served by Cloudflare's edge regardless of where the app runs
  if (vars.has('R2_PUBLIC_STORAGE_DOMAIN')) {
    p.log.warn(
      chalk.bold('Cloudflare Image Resizing required\n') +
        'Enable Image Resizing on the Cloudflare zone serving your R2 storage domain\n' +
        `(e.g. the zone for ${vars.get('R2_PUBLIC_STORAGE_DOMAIN')}).\n` +
        'Dashboard: Speed > Optimization > Image Resizing\n' +
        'This is used to compress images for video generation (Kling 10MB limit).'
    );
  }

  // -------------------------------------------------------------------------
  // Google OAuth
  // -------------------------------------------------------------------------
  const googleKeys = ['GOOGLE_CLIENT_ID', 'GOOGLE_CLIENT_SECRET'] as const;
  const hasGoogle = googleKeys.some((k) => vars.has(k));

  if (hasGoogle) {
    for (const k of googleKeys) {
      if (vars.has(k)) p.log.success(`${k} — already configured`);
    }
  }

  const setupGoogle =
    hasGoogle ||
    checkCancel(
      await p.confirm({
        message: 'Set up Google sign-in? (or use email OTP)',
        initialValue: false,
      })
    );

  if (setupGoogle) {
    await promptForKey(
      'GOOGLE_CLIENT_ID',
      'Google OAuth Client ID',
      'Get one at: https://console.cloud.google.com/apis/credentials'
    );
    await promptForKey('GOOGLE_CLIENT_SECRET', 'Google OAuth Client Secret');
  }

  // -------------------------------------------------------------------------
  // Observability (Langfuse)
  // -------------------------------------------------------------------------
  const langfuseKeys = [
    'LANGFUSE_PUBLIC_KEY',
    'LANGFUSE_SECRET_KEY',
    'LANGFUSE_BASE_URL',
  ] as const;
  const hasLangfuse = langfuseKeys.some((k) => vars.has(k));

  if (hasLangfuse) {
    for (const k of langfuseKeys) {
      if (vars.has(k)) p.log.success(`${k} — already configured`);
    }
  }

  const setupLangfuse =
    hasLangfuse ||
    checkCancel(
      await p.confirm({
        message:
          'Set up Langfuse for prompt management & LLM tracing? (optional)',
        initialValue: false,
      })
    );

  if (setupLangfuse) {
    await promptForKey(
      'LANGFUSE_PUBLIC_KEY',
      'Langfuse Public Key',
      'Get one at: https://cloud.langfuse.com/project/settings'
    );
    await promptForKey('LANGFUSE_SECRET_KEY', 'Langfuse Secret Key');
    await promptForKey(
      'LANGFUSE_BASE_URL',
      'Langfuse Base URL (leave empty for cloud)',
      'Default: https://cloud.langfuse.com'
    );

    if (!vars.has('LANGFUSE_PROMPTS_ENABLED')) {
      const enablePrompts = checkCancel(
        await p.confirm({
          message:
            'Fetch prompts from Langfuse API? (If no, prompts are served from git)',
          initialValue: false,
        })
      );
      vars.set('LANGFUSE_PROMPTS_ENABLED', enablePrompts ? 'true' : 'false');
      saveProgress();
    } else {
      p.log.success('LANGFUSE_PROMPTS_ENABLED — already configured');
    }

    const uploadPrompts = checkCancel(
      await p.confirm({
        message: 'Upload all prompts to Langfuse now?',
        initialValue: true,
      })
    );

    if (uploadPrompts) {
      const promptSpinner = p.spinner();
      promptSpinner.start('Uploading prompts to Langfuse');
      try {
        execFileSync('bun', ['scripts/upload-all-prompts.ts'], {
          stdio: 'pipe',
          cwd: process.cwd(),
        });
        promptSpinner.stop('Prompts uploaded to Langfuse');
      } catch {
        promptSpinner.stop('Prompt upload failed');
        p.log.warn(
          'You can retry later with: bun scripts/upload-all-prompts.ts'
        );
      }
    }
  }

  // -------------------------------------------------------------------------
  // Analytics (PostHog)
  // -------------------------------------------------------------------------
  const posthogKeys = ['VITE_PUBLIC_POSTHOG_PROJECT_TOKEN'] as const;
  const hasPostHog = posthogKeys.some((k) => vars.has(k));

  if (hasPostHog) {
    for (const k of posthogKeys) {
      if (vars.has(k)) p.log.success(`${k} — already configured`);
    }
    if (vars.has('VITE_PUBLIC_POSTHOG_HOST'))
      p.log.success('VITE_PUBLIC_POSTHOG_HOST — already configured');
  }

  const setupPostHog =
    hasPostHog ||
    checkCancel(
      await p.confirm({
        message:
          'Set up PostHog for product analytics & error tracking? (optional)',
        initialValue: false,
      })
    );

  if (setupPostHog) {
    await promptForKey(
      'VITE_PUBLIC_POSTHOG_PROJECT_TOKEN',
      'PostHog Project API Key',
      'Get one at: https://us.posthog.com/settings/project'
    );
    await promptForKey(
      'VITE_PUBLIC_POSTHOG_HOST',
      'PostHog Host (leave empty for default)',
      'Default: https://us.i.posthog.com'
    );
  }

  // -------------------------------------------------------------------------
  // Billing (Stripe)
  // -------------------------------------------------------------------------
  const stripeKeys = ['STRIPE_SECRET_KEY', 'STRIPE_WEBHOOK_SECRET'] as const;
  const hasStripe = stripeKeys.some((k) => vars.has(k));

  if (hasStripe) {
    for (const k of stripeKeys) {
      if (vars.has(k)) p.log.success(`${k} — already configured`);
    }
  }

  const setupBilling =
    hasStripe ||
    checkCancel(
      await p.confirm({
        message: 'Set up Stripe billing?',
        initialValue: false,
      })
    );

  if (setupBilling) {
    await promptForKey(
      'STRIPE_SECRET_KEY',
      'Stripe Secret Key (sk_live_...)',
      'Get one at: https://dashboard.stripe.com/apikeys'
    );
    await promptForKey(
      'STRIPE_WEBHOOK_SECRET',
      'Stripe Webhook Secret (whsec_...)',
      'From your webhook endpoint in the Stripe dashboard'
    );

    const appUrl = vars.get('VITE_APP_URL') ?? '<your-app-url>';
    p.note(
      [
        `Endpoint URL: ${appUrl}/api/billing/webhook`,
        '',
        'Events to enable:',
        '  - checkout.session.completed',
        '  - payment_intent.succeeded',
        '',
        'If using a restricted key, required permissions:',
        '  Charges (Read), Customers (Write),',
        '  Payment Intents (Read), Checkout Sessions (Write)',
      ].join('\n'),
      'Production Webhook Setup'
    );
  }

  // -------------------------------------------------------------------------
  // Write env file
  // -------------------------------------------------------------------------
  writeEnvFile(vars);
  p.log.success(`Wrote ${ENV_FILE}`);

  // -------------------------------------------------------------------------
  // Summary
  // -------------------------------------------------------------------------
  const features: Array<[string, string]> = [
    ['Hosting', 'Cloudflare Workers'],
    ['Database', 'Cloudflare D1'],
    ['Auth', 'Better Auth (secret generated)'],
    [
      'Email',
      vars.has('EMAIL_FROM') ? 'Cloudflare Email Service' : 'Not configured',
    ],
    ['AI Generation', vars.has('FAL_KEY') ? 'Configured' : 'Skipped'],
    ['Script Analysis', vars.has('OPENROUTER_KEY') ? 'Configured' : 'Skipped'],
    [
      'Storage',
      vars.has('R2_PUBLIC_STORAGE_DOMAIN') ? 'Configured' : 'Skipped',
    ],
    ['Google OAuth', vars.has('GOOGLE_CLIENT_ID') ? 'Configured' : 'Skipped'],
    [
      'Langfuse',
      vars.has('LANGFUSE_PUBLIC_KEY')
        ? 'Configured'
        : 'Skipped (using local prompts)',
    ],
    [
      'PostHog',
      vars.has('VITE_PUBLIC_POSTHOG_PROJECT_TOKEN') ? 'Configured' : 'Skipped',
    ],
    ['Stripe', vars.has('STRIPE_SECRET_KEY') ? 'Configured' : 'Not configured'],
  ];

  const summaryLines = features
    .map(
      ([name, status]) =>
        `${status.includes('Skip') || status.includes('Not configured') ? '○' : '●'} ${name}: ${status}`
    )
    .join('\n');

  p.note(summaryLines, 'Configuration Summary');

  await deploySetup(vars, checkCancel);
  p.outro(`Production config written to ${chalk.bold(ENV_FILENAME)}`);
}
