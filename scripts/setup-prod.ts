/**
 * Production & deployment setup for OpenStory — invoked via `bun setup`:
 *
 *   bun setup --prod        configure .env.production interactively + deploy
 *   bun setup --deploy      push secrets + deploy using existing .env.production
 *   bun setup --pr-preview  derive preview secrets and push to the GitHub
 *                           "staging" environment used by PR preview deploys
 *
 * Local development needs none of this — see scripts/setup.ts.
 */

