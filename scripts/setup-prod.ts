function execGh(args: string[]): string {
  return execFileSync('gh', args, {
    encoding: 'utf-8',
    stdio: ['pipe', 'pipe', 'pipe'],
  }).trim();
}

function bucketHasDomain(bucket: string, domain: string): boolean {
  try {
    const output = execFileSync(
      'bunx',
      ['wrangler', 'r2', 'bucket', 'domain', 'list', bucket],
      { stdio: ['pipe', 'pipe', 'pipe'], encoding: 'utf-8' }
    );
    return output.includes(domain);
  } catch {
    return false;
  }
}

async function getCloudflareWorkersSubdomain(
  accountId: string,
  apiToken: string
): Promise<string | undefined> {
  try {
    const res = await fetch(
      `https://api.cloudflare.com/client/v4/accounts/${accountId}/workers/subdomain`,
      { headers: { Authorization: `Bearer ${apiToken}` } }
    );
    const data: { success?: boolean; result?: { name?: string } } =
      await res.json();
    if (data.success && data.result?.name) return data.result.name;
  } catch {
    // fall through
  }
  return undefined;
}

async function prPreviewSetup() {
  p.intro(chalk.bold('OpenStory PR Preview Setup'));
  p.log.info(
    'Derives preview secrets from .env.production and pushes to GitHub "staging" environment.'
  );

  // 1. Check gh CLI
  if (!commandExists('gh')) {
    p.log.error(
      'GitHub CLI (gh) is not installed.\n' +
        'Install it: https://cli.github.com/'
    );
    process.exit(1);
  }

  try {
    execGh(['auth', 'status']);
  } catch {
    p.log.error('Not authenticated with gh. Run: gh auth login');
    process.exit(1);
  }
  p.log.success('GitHub CLI authenticated');

  // 2. Detect repo
  let repoName: string;
  try {
    const repoJson = execGh([
      'repo',
      'view',
      '--json',
      'nameWithOwner',
      '-q',
      '.nameWithOwner',
    ]);
    repoName = repoJson;
  } catch {
    p.log.error(
      'Could not detect GitHub repository. Are you in a git repo with a GitHub remote?'
    );
    process.exit(1);
  }
  p.log.success(`Repository: ${repoName}`);

  // 3. Load .env.production as base, then overlay .env.staging if it exists
  const prodFile = resolve(process.cwd(), '.env.production');
  const prodVars = parseEnvFile(prodFile);

  if (prodVars.size === 0) {
    p.log.error(
      'No .env.production found. Run `bun setup --prod` first to configure production values.'
    );
    process.exit(1);
  }

  const stagingFile = resolve(process.cwd(), '.env.staging');
  const stagingVars = parseEnvFile(stagingFile);

  const merged = new Map(prodVars);
  // .env.staging values take precedence over .env.production
  for (const [key, value] of stagingVars) {
    merged.set(key, value);
  }

  if (stagingVars.size > 0) {
    p.log.success(
      `Loaded ${prodVars.size} values from .env.production, ${stagingVars.size} overrides from .env.staging`
    );
  } else {
    p.log.info(`Loaded ${prodVars.size} values from .env.production`);
  }

  // 3b. Fallback to .env.local for account-level R2 credentials
  const localFile = resolve(process.cwd(), '.env.local');
  const localVars = parseEnvFile(localFile);
  const R2_FALLBACK_KEYS = ['R2_ACCESS_KEY_ID', 'R2_SECRET_ACCESS_KEY'];
  for (const key of R2_FALLBACK_KEYS) {
    const localVal = localVars.get(key);
    if (!merged.get(key) && localVal) {
      merged.set(key, localVal);
    }
  }

  // 3c. Auto-derive R2_ACCOUNT_ID from CLOUDFLARE_ACCOUNT_ID (same value)
  const cfAccountId = merged.get('CLOUDFLARE_ACCOUNT_ID');
  if (!merged.get('R2_ACCOUNT_ID') && cfAccountId) {
    merged.set('R2_ACCOUNT_ID', cfAccountId);
  }

  // 4. Auto-set preview overrides
  const removed = [
    'VITE_APP_URL',
    'GOOGLE_CLIENT_ID',
    'GOOGLE_CLIENT_SECRET',
    'STRIPE_SECRET_KEY',
    'STRIPE_WEBHOOK_SECRET',
  ];
  for (const key of removed) {
    merged.delete(key);
  }
  p.log.info(`Removed (not needed for preview): ${removed.join(', ')}`);

  // 5. Prompt for AI keys — skip if .env.staging already has its own values
  const aiKeys = ['FAL_KEY', 'OPENROUTER_KEY'] as const;
  const stagingHasAiKeys = aiKeys.every((k) => stagingVars.get(k));
  const hasAiKeys = aiKeys.every((k) => merged.get(k));

  if (stagingHasAiKeys) {
    p.log.success('AI keys already configured in .env.staging');
  } else if (hasAiKeys) {
    const reuseProd = await p.confirm({
      message: 'Use the same FAL_KEY and OPENROUTER_KEY as production?',
      initialValue: true,
    });

    if (p.isCancel(reuseProd)) {
      p.cancel('PR preview setup cancelled.');
      process.exit(0);
    }

    if (!reuseProd) {
      for (const key of aiKeys) {
        const value = await p.text({
          message: `${key} for preview`,
          placeholder: `Paste your ${key} here…`,
        });

        if (p.isCancel(value)) {
          p.cancel('PR preview setup cancelled.');
          process.exit(0);
        }

        if (value.trim()) {
          merged.set(key, value.trim());
        }
      }
    }
  }

  // 6. Derive staging R2 domains from production VITE_APP_URL
  const appUrl = prodVars.get('VITE_APP_URL') ?? '';
  let baseDomain = '';
  try {
    const host = new URL(appUrl).hostname;
    const parts = host.split('.');
    baseDomain = parts.length >= 2 ? parts.slice(-2).join('.') : host;
  } catch {
    // fall through
  }

  if (baseDomain) {
    merged.set('R2_PUBLIC_STORAGE_DOMAIN', `storage-stg.${baseDomain}`);
    merged.set('VITE_R2_PUBLIC_ASSETS_DOMAIN', `assets.${baseDomain}`);
  } else {
    p.log.warn(
      'Could not derive R2 domains from VITE_APP_URL. You may need to set R2_PUBLIC_STORAGE_DOMAIN and VITE_R2_PUBLIC_ASSETS_DOMAIN manually in GitHub.'
    );
  }

  // 6b. Derive staging bucket names (with -stg suffix, from production values only)
  const prodStorageBucket =
    prodVars.get('R2_BUCKET_NAME') ?? 'openstory-storage';
  const prodAssetsBucket =
    prodVars.get('R2_PUBLIC_ASSETS_BUCKET') ?? 'openstory-public-assets';
  merged.set('R2_BUCKET_NAME', `${prodStorageBucket}-stg`);
  merged.set('R2_PUBLIC_ASSETS_BUCKET', prodAssetsBucket);

  p.log.info(
    `R2_PUBLIC_STORAGE_DOMAIN = ${merged.get('R2_PUBLIC_STORAGE_DOMAIN') ?? '(not set)'}\n` +
      `VITE_R2_PUBLIC_ASSETS_DOMAIN  = ${merged.get('VITE_R2_PUBLIC_ASSETS_DOMAIN') ?? '(not set)'}`
  );

  // Offer to attach custom domains to R2 buckets
  const stgStorageDomain = merged.get('R2_PUBLIC_STORAGE_DOMAIN');
  const stgAssetsDomain = merged.get('VITE_R2_PUBLIC_ASSETS_DOMAIN');

  if (stgStorageDomain || stgAssetsDomain) {
    const attachDomains = await p.confirm({
      message: 'Attach these custom domains to R2 buckets via Cloudflare?',
      initialValue: true,
    });

    if (p.isCancel(attachDomains)) {
      p.cancel('PR preview setup cancelled.');
      process.exit(0);
    }

    if (attachDomains) {
      // Auto-detect zone ID from domain
      let zoneId = prodVars.get('CLOUDFLARE_ZONE_ID');
      const apiToken = prodVars.get('CLOUDFLARE_API_TOKEN');

      if (!zoneId && apiToken && baseDomain) {
        const zoneSpinner = p.spinner();
        zoneSpinner.start(`Looking up zone ID for ${baseDomain}`);
        try {
          const res = await fetch(
            `https://api.cloudflare.com/client/v4/zones?name=${baseDomain}`,
            { headers: { Authorization: `Bearer ${apiToken}` } }
          );
          const data: { success?: boolean; result?: { id: string }[] } =
            await res.json();
          const firstZone = data.success ? data.result?.[0] : undefined;
          if (firstZone) {
            zoneId = firstZone.id;
            zoneSpinner.stop(
              `Found zone ID for ${baseDomain}: ${zoneId.slice(0, 8)}…`
            );
          } else {
            zoneSpinner.stop(`Could not find zone for ${baseDomain}`);
          }
        } catch {
          zoneSpinner.stop('Zone lookup failed');
        }
      }

      if (!zoneId) {
        const manualZoneId = await p.text({
          message:
            'Cloudflare Zone ID\n' +
            chalk.dim('Find in Cloudflare Dashboard > your domain > Overview'),
          placeholder: 'Paste your CLOUDFLARE_ZONE_ID here…',
        });

        if (p.isCancel(manualZoneId)) {
          p.cancel('PR preview setup cancelled.');
          process.exit(0);
        }

        zoneId = manualZoneId.trim() || undefined;
      }

      if (zoneId) {
        // Use already-derived staging bucket names (set in step 6b)
        const storageBucket =
          merged.get('R2_BUCKET_NAME') ?? 'openstory-storage-stg';
        const assetsBucket =
          merged.get('R2_PUBLIC_ASSETS_BUCKET') ?? 'openstory-public-assets';

        for (const [domain, bucket] of [
          [stgStorageDomain, storageBucket],
          [stgAssetsDomain, assetsBucket],
        ] as const) {
          if (!domain) continue;
          if (bucketHasDomain(bucket, domain)) {
            p.log.success(`${domain} → ${bucket} (already attached)`);
            continue;
          }
          try {
            execFileSync(
              'bunx',
              [
                'wrangler',
                'r2',
                'bucket',
                'domain',
                'add',
                bucket,
                '--domain',
                domain,
                '--zone-id',
                zoneId,
                '--force',
              ],
              { stdio: 'inherit' }
            );
            p.log.success(`Attached ${domain} → ${bucket}`);
          } catch {
            p.log.warn(`Could not attach ${domain} to ${bucket}`);
          }
        }
      } else {
        p.log.warn('No zone ID — skipping domain attachment.');
      }
    }
  }

  // 6c. Configure CORS on the staging bucket. Each PR deploy gets a unique
  // workers.dev URL so we use a wildcard origin matching the preview pattern.
  // GET/HEAD only — staging buckets don't accept browser uploads.
  const stgBucketName = merged.get('R2_BUCKET_NAME');
  if (stgBucketName) {
    let workersSubdomain: string | undefined;
    const accountId = merged.get('CLOUDFLARE_ACCOUNT_ID');
    const apiToken = merged.get('CLOUDFLARE_API_TOKEN');
    if (accountId && apiToken) {
      const subSpinner = p.spinner();
      subSpinner.start('Looking up Cloudflare Workers subdomain');
      workersSubdomain = await getCloudflareWorkersSubdomain(
        accountId,
        apiToken
      );
      if (workersSubdomain) {
        subSpinner.stop(`Workers subdomain: ${workersSubdomain}`);
      } else {
        subSpinner.stop('Could not auto-detect Workers subdomain');
      }
    }

    if (!workersSubdomain) {
      const manual = await p.text({
        message:
          'Workers subdomain (the part before .workers.dev in your preview URL)',
        placeholder: 'e.g. myaccount',
      });
      if (!p.isCancel(manual) && manual.trim()) {
        workersSubdomain = manual.trim();
      }
    }

    const previewOrigins = derivePreviewOriginPatterns({ workersSubdomain });

    if (previewOrigins.length === 0) {
      p.log.warn(
        `No Workers subdomain available.\n` +
          `Configure CORS manually in Cloudflare Dashboard:\n` +
          `  R2 → ${stgBucketName} → Settings → CORS Policy`
      );
    } else {
      const setupStgCors = await p.confirm({
        message: `Configure CORS on staging bucket ${chalk.bold(stgBucketName)}?`,
        initialValue: true,
      });

      if (p.isCancel(setupStgCors)) {
        p.cancel('PR preview setup cancelled.');
        process.exit(0);
      }

      if (setupStgCors) {
        const rules = buildRules({
          origins: previewOrigins,
          includeWrites: false,
        });
        const corsSpinner = p.spinner();
        corsSpinner.start(`Configuring CORS on ${stgBucketName}`);
        try {
          setBucketCors(stgBucketName, rules);
          corsSpinner.stop(
            `CORS configured on ${stgBucketName} (${previewOrigins.join(', ')})`
          );
        } catch {
          corsSpinner.stop('CORS configuration failed');
          p.log.warn(
            'Configure manually in Cloudflare Dashboard:\n' +
              `  R2 → ${stgBucketName} → Settings → CORS Policy\n\n` +
              JSON.stringify({ rules }, null, 2)
          );
        }
      }
    }
  }

  // 7. Summary + confirm
  const previewSecrets = getPrPreviewSecrets();
  const allKeys = [...previewSecrets, ...PR_PREVIEW_VARIABLES];
  const ready = allKeys.filter((k) => merged.get(k));
  const missing = allKeys.filter((k) => !merged.get(k));

  p.log.info(
    `${chalk.green(String(ready.length))} keys ready, ${chalk.yellow(String(missing.length))} missing`
  );

  if (missing.length > 0) {
    p.log.warn(`Missing: ${missing.join(', ')}`);
  }

  // Confirm before pushing to GitHub
  const confirmPush = await p.confirm({
    message: `Push ${ready.length} keys to GitHub "staging" environment?`,
    initialValue: true,
  });

  if (p.isCancel(confirmPush) || !confirmPush) {
    p.cancel('PR preview setup cancelled.');
    process.exit(0);
  }

  // 8. Save .env.staging locally
  const stagingLines = [
    '# =============================================================================',
    '# Staging Configuration',
    '# Generated by `bun setup --pr-preview` — safe to edit',
    '# =============================================================================',
    '',
  ];
  for (const [key, value] of merged) {
    stagingLines.push(`${key}=${value}`);
  }
  stagingLines.push('');
  writeFileSync(stagingFile, stagingLines.join('\n'));
  p.log.success(`Wrote ${stagingFile}`);

  // 9. Push to GitHub staging environment
  const envSpinner = p.spinner();
  envSpinner.start('Ensuring GitHub "staging" environment exists');
  try {
    execGh([
      'api',
      '--method',
      'PUT',
      `/repos/${repoName}/environments/staging`,
    ]);
    envSpinner.stop('GitHub "staging" environment ready');
  } catch (error) {
    envSpinner.stop('Failed to create staging environment');
    p.log.error(
      `Error: ${error instanceof Error ? error.message : String(error)}\n` +
        'You may need to create it manually in GitHub Settings > Environments.'
    );
    process.exit(1);
  }

  // Push secrets
  const secretSpinner = p.spinner();
  const secretsToPush = previewSecrets.filter((k) => merged.get(k));
  secretSpinner.start(
    `Pushing ${secretsToPush.length} secrets to staging environment`
  );
  let secretsPushed = 0;
  let secretsFailed = 0;

  for (const key of secretsToPush) {
    const value = merged.get(key);
    if (!value) continue;
    try {
      execFileSync(
        'gh',
        ['secret', 'set', key, '--env', 'staging', '--body', value],
        {
          stdio: ['pipe', 'pipe', 'pipe'],
        }
      );
      secretsPushed++;
    } catch {
      secretsFailed++;
      p.log.warn(`Failed to push secret: ${key}`);
    }
  }

  if (secretsFailed > 0) {
    secretSpinner.stop(
      `Pushed ${secretsPushed} secrets (${secretsFailed} failed)`
    );
  } else {
    secretSpinner.stop(`Pushed ${secretsPushed} secrets`);
  }

  // Push variables
  const varsToPush = PR_PREVIEW_VARIABLES.filter((k) => merged.get(k));
  if (varsToPush.length > 0) {
    const varSpinner = p.spinner();
    varSpinner.start(
      `Pushing ${varsToPush.length} variables to staging environment`
    );
    let varsPushed = 0;
    let varsFailed = 0;

    for (const key of varsToPush) {
      const value = merged.get(key);
      if (!value) continue;
      try {
        execFileSync(
          'gh',
          ['variable', 'set', key, '--env', 'staging', '--body', value],
          {
            stdio: ['pipe', 'pipe', 'pipe'],
          }
        );
        varsPushed++;
      } catch {
        varsFailed++;
        p.log.warn(`Failed to push variable: ${key}`);
      }
    }

    if (varsFailed > 0) {
      varSpinner.stop(`Pushed ${varsPushed} variables (${varsFailed} failed)`);
    } else {
      varSpinner.stop(`Pushed ${varsPushed} variables`);
    }
  }

  // 9. Summary
  p.note(
    [
      `Repository: ${repoName}`,
      `Environment: staging`,
      `Secrets pushed: ${secretsPushed}`,
      `Variables pushed: ${varsToPush.length}`,
    ].join('\n'),
    'PR Preview Setup Complete'
  );

  p.outro('Done! PR preview deploys will now use the staging environment.');
}

